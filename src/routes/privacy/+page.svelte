	<h2 id="collectinfo">1. 서비스 이용을 위하여 수집하는 개인정보</h2>
	<div>
		당사의 서비스 이용을 위해 당사는 사용자의 정보를 수집합니다.
		<ul>
			<li>
				수집하는 개인정보: 이메일, 서비스 이용 기록, 접속 로그, 쿠키, IP, 휴대전화번호, 기기 고유
				번호
			</li>
			<li>수집 방법: 브라우저를 통해 수집, 사용자가 직접 기입하여 수집</li>
			<li>보유 기간: 6시간 (부정행위로 이용 정지 처분을 받은 경우 최대 2주간 보관될수 있습니다)</li>
			<li>수집 목적: 서비스의 안정적인 제공을 위함</li>
			<li>
				관련법령: 소비자의 불만 또는 분쟁처리에 관한 기록, 대금결제 및 재화 등의 공급에 관한 기록,
				계약 또는 청약철회 등에 관한 기록
			</li>
		</ul>
	</div>
	<h3 id="cookiedisallow">[쿠키 수집 거부에 관한 내용]</h3>
	<div>
		이용자는 아래 방법으로 쿠키 자동수집을 거부할 수 있습니다.<br />
		a) Internet Explorer : 웹브라우저 상단 도구 → 인터넷옵션 → 개인정보 → 직접 설정 <br />
		b) Chrome : 웹브라우저 우측 상단 아이콘 → 설정 → 고급 설정 표시 → 개인정보 섹션의 콘텐츠 설정 버튼
		→ 쿠키 섹션에서 직접 설정<br />
		c) Safari : 웹브라우저 상단 메뉴 → 환경 설정 → 개인 정보 보호 → 쿠키 및 웹 사이트 데이터 → 직접 설정<br
		/>
		d) Firefox : 웹브라우저 우측 상단 아이콘 → 설정 → 개인 정보 및 보안 → 쿠키와 사이트 데이터 → 직접
		설정<br />
		단, 고객이 쿠키 설치를 거부하였을 경우 당사 웹사이트 이용에 제약이 있을 수 있습니다.
	</div>
