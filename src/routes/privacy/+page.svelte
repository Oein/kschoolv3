	<h2 id="secureofinfo">5. 개인정보보호를 위한 안전성 확보 조치</h2>
	<div>
		당사는 개인정보보호법 제29조에 따라 다음과 같이 안전성 확보에 필요한 기술적/관리적 조치를
		취합니다.

		<ul>
			<li>정기적으로 개인정보 취급 관련 자체 감사를 실시하고 있습니다.</li>
			<li>개인정보 취급 직원을 최소화하여 개인정보를 관리하는 대책을 시행하고 있습니다.</li>
			<li>개인정보의 안전한 처리를 위하여 내부관리계획을 수립하고 시행하고 있습니다.</li>
			<li>
				이용자의 비밀번호는 저장되지 않으며, 중요한 데이터는 파일 및 전송 데이터를 암호화 하거나
				파일 잠금 기능을 사용하는 등의 별도 보안기능을 사용하고 있습니다.
			</li>
			<li>
				개인정보처리시스템에 접속한 기록을 최소 6개월 이상 보관, 관리하고 있으며, 접속 기록이 위변조
				및 도난, 분실되지 않도록 보안기능을 사용하고 있습니다.
			</li>
			<li>개인정보가 포함된 보조저장매체 등을 안전한 장소에 보관하고 있습니다.</li>
		</ul>
	</div>
	<h2 id="removeinfo">6. 개인정보 주체의 권리와 의무</h2>
	<div>
		<ul>
			<li>
				정보주체는 당사에 대해 언제든지 개인정보 열람, 정정, 삭제, 처리정지 요구 등의 권리를 행사할
				수 있습니다.
			</li>
			<li>
				제1항에 따른 권리 행사는 당사에 대해 개인정보 보호법 시행령 제41조제1항에 따라 전자우편 등을
				통하여 하실 수 있으며 당사는 이에 대해 지체 없이 조치하겠습니다.
			</li>
			<li>
				제1항에 따른 권리 행사는 정보주체의 법정대리인이나 위임을 받은 자 등 대리인을 통하여 하실 수
				있습니다. 이 경우 개인정보 보호법 시행규칙 별지 제11호 서식에 따른 위임장을 제출하여야
				합니다.
			</li>
			<li>
				개인정보 열람 및 처리정지 요구는 개인정보보호법 제35조 제5항, 제37조 제2항에 의하여
				정보주체의 권리가 제한 될 수 있습니다.
			</li>
			<li>
				개인정보의 정정 및 삭제 요구는 다른 법령에서 그 개인정보가 수집 대상으로 명시되어 있는
				경우에는 그 삭제를 요구할 수 없습니다.
			</li>
			<li>
				당사는 정보주체 권리에 따른 열람의 요구, 정정·삭제의 요구, 처리정지의 요구 시 열람 등 요구를
				한 자가 본인이거나 정당한 대리인인지를 확인합니다.
			</li>
			<li>
				삭제를 원할 경우 <a href="#adminofinfo">개인정보 보호 책임자</a>에게 연락하여 삭제를 요청할
				수 있습니다.
			</li>
		</ul>
	</div>
